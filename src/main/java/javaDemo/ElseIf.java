package javaDemo;

public class ElseIf {

	public static void main(String[] args) {

		int x=10;
		int y=20;
		
		if(x>y) {
			
			System.out.println("x is greater y");
		}else if(x>=y) {
			
			System.out.println("x is greater than or = y");
			
		}else if(x<y) {
			System.out.println("x is smller than y");
		}else {
			System.out.println("x is = y");
		}
		
			
			

	}

}
